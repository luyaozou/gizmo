class FFTBox(QtGui.QGroupBox):
    '''
        FFT setting box
    '''

    def __init__(self, parent):
        QtGui.QWidget.__init__(self, parent)
        self.parent = parent

        self.setTitle('FFT Setting')
        self.setAlignment(QtCore.Qt.AlignLeft)
